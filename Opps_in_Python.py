'''Create a class Employee with attributes name, id, salary and methods
get_name(), get_id(), get_salary(), set_salary(), and calculate_bonus().
Create an object of the Employee class and demonstrate the use of all the methods.'''
class Employee:
    def __init__(self, name, id, salary):
        self.name = name
        self.id = id
        self.salary = salary
    def get_name(self):
        return self.name
    def get_id(self):
        return self.id
    def get_salary(self):
        return self.salary
    def set_salary(self, new_salary):
        self.salary = new_salary
    def calculate_bonus(self):
        return self.salary * 0.1
E= Employee('Akash', 325004, 250000)
print("Name of the employee: ",E.get_name())
print("the  EID is: ",E.get_id())
print("salary of employee is: ",E.get_salary())
E.set_salary(260000)
print("the new salary of the employee is :",E.get_salary())
print("the bonus of the employee is :" ,E.calculate_bonus())
